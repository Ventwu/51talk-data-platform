type FormControlType = 'input' | 'textarea' | 'select' | 'number' | 'color' | 'switch' | 'slider' | 'checkbox';

interface FormControlProps {
  type: FormControlType;
  label: string;
  value: any;
  onChange: (value: any) => void;
  options?: Array<{ label: string; value: any }>;
  min?: number;
  max?: number;
  step?: number;
  placeholder?: string;
  disabled?: boolean;
  description?: string;
}

// ÈÄöÁî®Ë°®ÂçïÊéß‰ª∂
const FormControl: React.FC<FormControlProps> = ({
  type,
  label,
  value,
  onChange,
  options = [],
  min,
  max,
  step = 1,
  placeholder,
  disabled = false,
  description
}) => {
  const renderControl = () => {
    switch (type) {
      case 'input':
        return (
          <input
            type="text"
            value={value || ''}
            onChange={(e) => onChange(e.target.value)}
            placeholder={placeholder}
            disabled={disabled}
            className="form-input"
          />
        );
      
      case 'textarea':
        return (
          <textarea
            value={value || ''}
            onChange={(e) => onChange(e.target.value)}
            placeholder={placeholder}
            disabled={disabled}
            className="form-textarea"
            rows={3}
          />
        );
      
      case 'select':
        return (
          <select
            value={value || ''}
            onChange={(e) => onChange(e.target.value)}
            disabled={disabled}
            className="form-select"
          >
            {options.map((option) => (
              <option key={option.value} value={option.value}>
                {option.label}
              </option>
            ))}
          </select>
        );
      
      case 'number':
        return (
          <input
            type="number"
            value={value || 0}
            onChange={(e) => onChange(Number(e.target.value))}
            min={min}
            max={max}
            step={step}
            disabled={disabled}
            className="form-number"
          />
        );
      
      case 'color':
        return (
          <input
            type="color"
            value={value || '#000000'}
            onChange={(e) => onChange(e.target.value)}
            disabled={disabled}
            className="form-color"
          />
        );
      
      case 'switch':
        return (
          <label className="form-switch">
            <input
              type="checkbox"
              checked={!!value}
              onChange={(e) => onChange(e.target.checked)}
              disabled={disabled}
            />
            <span className="switch-slider"></span>
          </label>
        );
      
      case 'slider':
        return (
          <div className="form-slider-container">
            <input
              type="range"
              value={value || 0}
              onChange={(e) => onChange(Number(e.target.value))}
              min={min || 0}
              max={max || 100}
              step={step}
              disabled={disabled}
              className="form-slider"
            />
            <span className="slider-value">{value}</span>
          </div>
        );
      
      case 'checkbox':
        return (
          <label className="form-checkbox">
            <input
              type="checkbox"
              checked={!!value}
              onChange={(e) => onChange(e.target.checked)}
              disabled={disabled}
            />
            <span className="checkbox-mark"></span>
          </label>
        );
      
      default:
        return null;
    }
  };

  return (
    <div className="form-control">
      <div className="control-header">
        <label className="control-label">{label}</label>
        {description && (
          <span className="control-description" title={description}>
            ‚ÑπÔ∏è
          </span>
        )}
      </div>
      <div className="control-input">
        {renderControl()}
      </div>
    </div>
  );
};

// Â±ûÊÄßÂàÜÁªÑÁªÑ‰ª∂
interface PropertyGroupComponentProps {
  group: PropertyGroup;
  children: React.ReactNode;
  collapsed?: boolean;
  onToggleCollapse?: (groupKey: string) => void;
}

const PropertyGroupComponent: React.FC<PropertyGroupComponentProps> = ({
  group,
  children,
  collapsed = false,
  onToggleCollapse
}) => {
  const handleToggle = () => {
    if (group.collapsible) {
      onToggleCollapse?.(group.key);
    }
  };

  return (
    <div className={`property-group ${collapsed ? 'collapsed' : ''}`}>
      <div 
        className={`group-header ${group.collapsible ? 'collapsible' : ''}`}
        onClick={handleToggle}
      >
        {group.icon && <span className="group-icon">{group.icon}</span>}
        <span className="group-title">{group.title}</span>
        {group.collapsible && (
          <span className="collapse-icon">
            {collapsed ? '‚ñ∂' : '‚ñº'}
          </span>
        )}
      </div>
      {!collapsed && (
        <div className="group-content">
          {children}
        </div>
      )}
    </div>
  );
};

